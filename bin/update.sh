#!/bin/bash

# Скрипт для обновления проекта vk2tg из Git на сервере
set -e

echo "🔄 Начинаем обновление проекта vk2tg..."

# Переходим в директорию проекта
cd "$(dirname "$0")/.."

# Сохраняем текущий статус
echo "📋 Проверяем текущий статус Git..."
git status

# Получаем последние изменения
echo "📥 Получаем изменения из Git..."
git fetch origin

# Показываем что изменилось
echo "📊 Изменения в репозитории:"
git log --oneline HEAD..origin/main | head -10

# Спрашиваем подтверждение
read -p "❓ Продолжить обновление? (y/n): " -n 1 -r
echo
if [[ ! $REPLY =~ ^[Yy]$ ]]; then
    echo "❌ Обновление отменено"
    exit 1
fi

# Обновляем код
echo "⬇️ Обновляем код..."
git pull origin main

# Обновляем зависимости
echo "📦 Обновляем зависимости Composer..."
composer install --no-dev --optimize-autoloader

# Проверяем что все работает
echo "✅ Проверяем работоспособность..."
php -l src/Sc/Vk2Tg.php || {
    echo "❌ Ошибка в PHP коде!"
    exit 1
}

echo "🎉 Обновление завершено успешно!"
echo "🏃 Перезапустите сервис для применения изменений"
